package com.lzbruby.lang;

/**
 * 符号常量
 */
public interface SignConstants {

    /** 逗号 */
    String COMMA = ",";

    /** 竖线分隔符 */
    String VERTICAL_BAR = "|";

    /** 作为 VERTICAL_BAR 做正则分隔符 */
    String VERTICAL_BAR_REGEX = "\\|";

    /** 点 */
    String DOT = ".";

    /** 冒号 */
    String COLON = ":";

    /** 分号 */
    String SEMICOLON = ";";

    /**  双引号 */
    String DOUBLE_QUOTE = "\"";

    /**  单引号 */
    String QUOTE = "'";

    /**  星号 */
    String ASTERISK = "*";

    /** 美元符 */
    String DOLLAR_SIGN = "$";

    /**  井号 */
    String SHARP = "#";

    /** 下划线 */
    String UNDERLINE = "_";

    /** 百分号 */
    String PERCENT_SIGN = "%";

    /** 与 */
    String AND = "&";

    /** 斜钱  */
    String SLASH = "/";
}
